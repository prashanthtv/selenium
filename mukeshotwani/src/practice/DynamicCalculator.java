package practice;

public class DynamicCalculator {

	
	
	public int add(int a, int b)
	{
		
		int c=a+b;
		return c;
		
	}
	
	public double sub(double x, double y)
	{
		
		double result  =x-y;
		return result;
		
	}
	
	public double Div(double x, double y)
	{
		
		double divresult= x*y;
		return divresult;
		
	}
	
	public int mult(int x, int y)
	{
		
		int multresult= x*y;
		return multresult;
		
	}
	
	
	
	
	
	
}



